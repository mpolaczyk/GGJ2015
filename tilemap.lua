function tilemapClass.new(pos, dim)
   local self = setmetatable({}, tilemapClass)
   self.pos = pos
   self.dim = dim
   self.tiles = {}
   for i=1,numRows,1 do
      local row = {}
      for j=1,numCols,1 do
 	 local position = {x = (j-1)*dim + self.pos.x,
			   y = (i-1)*dim + self.pos.y}
	 if tilemapPattern[i][j] == 0 then
	    table.insert(row, pathTileClass.new(position,
						dim))
	 end
	 if tilemapPattern[i][j] == 1 then
	    table.insert(row, wallTileClass.new(position,
						dim))
	 end
      end
      table.insert(self.tiles, row)
   end
   return self
end

function tilemapClass:draw()
   for i=1,numRows,1 do
      for j=1,numCols,1 do
	 self.tiles[i][j]:draw()
      end
   end
end

-- returns position of upper left corner of given tile
-- ranges starting from 1, lua style!
function tilemapClass:getPos(tilex, tiley)
   return {x = (tilex-1)*self.dim + self.pos.x,
	   y = (tiley-1)*self.dim + self.pos.y}
end

